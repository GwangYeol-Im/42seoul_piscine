int		ft_is_prime(int nb)
{
	int	div;

	div = 2;
	if (nb < 2)
		return (0);
	while (div <= nb)
	{
		if (nb % div == 0)
		{
			return (nb == div ? 1 : 0);
		}
		div++;
	}
	return (0);
}
