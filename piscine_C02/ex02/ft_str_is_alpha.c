int		ft_str_is_alpha(char *str)
{
	int answer;

	answer = 1;
	while (*str != '\0')
	{
		if (!(*str >= 65 && *str <= 122))
		{
			answer = 0;
		}
		else if (*str >= 91 && *str <= 96)
		{
			answer = 0;
		}
		str++;
	}
	return (answer);
}
