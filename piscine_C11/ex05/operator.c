long long	add(long long n1, long long n2)
{
	return (n1 + n2);
}

long long	min(long long n1, long long n2)
{
	return (n1 - n2);
}

long long	mul(long long n1, long long n2)
{
	return (n1 * n2);
}

long long	div(long long n1, long long n2)
{
	return (n1 / n2);
}

long long	mod(long long n1, long long n2)
{
	return (n1 % n2);
}
