int		ft_count_if(char **tab, int length, int (*f)(char*))
{
	int	answer;
	int	i;

	answer = 0;
	i = 0;
	while (tab[i])
	{
		if (f(tab[i++]))
			answer++;
	}
	return (answer);
}
